function add(nb1, nb2) {
    return nb1 + nb2;
}

function multiply(nb1, nb2) {
    return nb1 * nb2;
}

function substract(nb1, nb2) {
    return nb1 - nb2;
}

function divide(nb1, nb2) {
    return nb1 / nb2;
}

module.exports = {
    add: add,
    multiply: multiply,
    substract: substract,
    divide: divide
}